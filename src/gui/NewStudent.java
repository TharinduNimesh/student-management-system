    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name = tbName.getText();
        String date = tbDate.getText();
        String email = tbEmail.getText();
        String address = tbAddress.getText();
        String mobile = tbMobile.getText();
        int gender = cbGender.getSelectedIndex();

        Vector vector = new Vector();
        vector.add(name);
        vector.add(date);
        vector.add(email);
        vector.add(address);
        vector.add(mobile);

        boolean isValid = true;

        for (var item : vector) {
            if (String.valueOf(item).isBlank()) {
                isValid = false;
            }
        }
        if (gender == 0) {
            isValid = false;
        }
        
        if(!isValid) {
            JOptionPane.showMessageDialog(this, "Please Fill All TextFields", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Student student = new Student();
        student.setName(name);
        student.setDate(date);
        student.setEmail(email);
        student.setAddress(address);
        student.setMobile(mobile);
        student.setGender(String.valueOf(gender));
        
        student.save();
        
        this.Clear();
        
        JOptionPane.showMessageDialog(this, "New Student Added", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton2ActionPerformed

