    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        this.student = null;
        String id = tbSID.getText();
        if (id.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter A Student ID", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Student student = new Student("id", id);
        if (student.getName() == null) {
            JOptionPane.showMessageDialog(this, "Invalid Student ID", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        tbName.setText(student.getName());
        tbDOB.setText(student.getDate());
        tbMobile.setText(student.getMobile());
        tbEmail.setText(student.getEmail());
        tbAddress.setText(student.getAddress());
        cbGender.setSelectedIndex(Integer.parseInt(student.getGender()));

        this.student = student;
    }//GEN-LAST:event_jButton2ActionPerformed

