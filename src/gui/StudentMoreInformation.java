    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.student == null) {
            JOptionPane.showMessageDialog(this, "Please search a student", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        this.student.remove();
        this.Clear();
        JOptionPane.showMessageDialog(this, "Successfully Removed", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String name = tbName.getText();
        String date = tbDOB.getText();
        String email = tbEmail.getText();
        String address = tbAddress.getText();
        String mobile = tbMobile.getText();
        int gender = cbGender.getSelectedIndex();

        Vector vector = new Vector();
        vector.add(name);
        vector.add(date);
        vector.add(email);
        vector.add(address);
        vector.add(mobile);

        boolean isValid = true;
        for (var item : vector) {
            if (String.valueOf(item).isBlank()) {
                isValid = false;
            }
        }

        if (gender == 0) {
            isValid = false;
        }

        if (!isValid) {
            JOptionPane.showMessageDialog(this, "Please search a student", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        this.student.setName(name);
        this.student.setAddress(address);
        this.student.setDate(date);
        this.student.setEmail(email);
        this.student.setMobile(mobile);
        this.student.setGender(String.valueOf(gender));
        
        this.student.save();
    }//GEN-LAST:event_jButton4ActionPerformed

