    private void loadStudents() {
        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            ResultSet students = RegularClass.getStudents(classId);
            while (students.next()) {
                Vector<String> row = new Vector<>();
                row.add(students.getString("id"));
                row.add(students.getString("name"));
                row.add(students.getString("has_paid").equals("1") ? "Yes" : "No");

                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

