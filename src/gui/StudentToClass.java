    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String id = tbSID.getText();
        if (id.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter A Student ID", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Student student = new Student("id", id);
        if (student.getName() == null) {
            JOptionPane.showMessageDialog(this, "Invalid Student ID", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }

        student.addToClass(classId);
        this.loadStudents();
    }//GEN-LAST:event_jButton2ActionPerformed

