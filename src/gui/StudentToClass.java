    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            int row = jTable1.getSelectedRow();
            if (String.valueOf(jTable1.getValueAt(row, 2)).equals("Yes")) {
                JOptionPane.showMessageDialog(this, "Aleady Have Paid", "WARNING", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            Student student = new Student("id", String.valueOf(jTable1.getValueAt(row, 0)));
            student.markAsPaid(classId);

            this.loadStudents();
        }
    }//GEN-LAST:event_jTable1MouseClicked

